package autogenerated

import java.io.InputStream

class LexerAnalyzer(private val stream: InputStream, ) {
	var position: Int = 0
	var currentChar: Int = -1
	var token: Token = Token.EOF
	var tokenValue: String = ""
	var tokenText: StringBuilder = StringBuilder()
	
	init {
		nextChar()
		nextToken()
	}
	
	fun nextToken(): Unit {
		skipWhitespaces()
		var string = ""
		var last: Char
		while (!currentChar.toChar().isWhitespace()) {
			if (currentChar < 0) {
				string = tokenText.toString()
				tokenText.clear()
				token = Token.EOF
				tokenValue = "EOF"
				break
			}
	
			last = currentChar.toChar()
			string = tokenText.toString()
			tokenText.append(currentChar.toChar())
			if (tokenText.toString().matches("[0-9]+".toRegex()) || tokenText.toString() == ";" || tokenText.toString() == "EPS" || tokenText.toString() == "=" || tokenText.toString() == "-" || tokenText.toString() == "/" || tokenText.toString() == ")" || tokenText.toString() == "*" || tokenText.toString().matches("[a-zA-Z][a-zA-Z0-9]*".toRegex()) || tokenText.toString() == "(" || tokenText.toString() == "^" || tokenText.toString() == "EOF" || tokenText.toString() == "+") {
				nextChar()
				if (currentChar < 0 || currentChar.toChar().isWhitespace()) {
					string = tokenText.toString()
					tokenText.clear()
					break
				}
	
			} else {
				tokenText.clear()
				tokenText.append(last)
				nextChar()
				break
			}
	
	
		}
	
		if (string.matches("[0-9]+".toRegex())) {
			token = Token.NUMBER
			tokenValue = string
			return
		}
	
		if (string == ";") {
			token = Token.SEMI
			tokenValue = string
			return
		}
	
		if (string == "EPS") {
			token = Token.EPS
			tokenValue = string
			return
		}
	
		if (string == "=") {
			token = Token.EQ
			tokenValue = string
			return
		}
	
		if (string == "-") {
			token = Token.MINUS
			tokenValue = string
			return
		}
	
		if (string == "/") {
			token = Token.DIV
			tokenValue = string
			return
		}
	
		if (string == ")") {
			token = Token.RB
			tokenValue = string
			return
		}
	
		if (string == "*") {
			token = Token.MULT
			tokenValue = string
			return
		}
	
		if (string.matches("[a-zA-Z][a-zA-Z0-9]*".toRegex())) {
			token = Token.VARIABLE
			tokenValue = string
			return
		}
	
		if (string == "(") {
			token = Token.LB
			tokenValue = string
			return
		}
	
		if (string == "^") {
			token = Token.POW
			tokenValue = string
			return
		}
	
		if (string == "EOF") {
			token = Token.EOF
			tokenValue = string
			return
		}
	
		if (string == "+") {
			token = Token.PLUS
			tokenValue = string
			return
		}
	
	}
	
	private fun nextChar(): Unit {
		currentChar = stream.read()
		position++
	}
	
	private fun skipWhitespaces(): Unit {
		while (currentChar.toChar().isWhitespace()) {
			nextChar()
		}
	
	}
}